let languageChanged = () => {
    switch (settings.language) {
        case "🇷🇺 Русский":
            State.variables.localization_ru();
            break;
        default:
            State.variables.localization_en();
            break;
    }
};

languageChanged();

Setting.addList("language", {
    label: "Language",
    list: ["🇷🇺 Русский", "🇺🇸 English"], 
    default: "🇷🇺 Русский",
    onInit: languageChanged,
    onChange: languageChanged
});
